/**
 * Cache Implementation Module
 * LRU cache for optimized search performance
 */

import { CACHE } from '../constants.js';

// Simple LRU cache implementation for optimized search
export class LRUCache {
  constructor(maxSize = 100) {
    this.maxSize = maxSize;
    this.cache = new Map();
  }

  get(key) {
    if (this.cache.has(key)) {
      const value = this.cache.get(key);
      this.cache.delete(key);
      this.cache.set(key, value);
      return value;
    }
    return null;
  }

  set(key, value) {
    if (this.cache.has(key)) {
      this.cache.delete(key);
    } else if (this.cache.size >= this.maxSize) {
      const firstKey = this.cache.keys().next().value;
      this.cache.delete(firstKey);
    }
    this.cache.set(key, value);
  }

  clear() {
    this.cache.clear();
  }

  size() {
    return this.cache.size;
  }
}

// Create caches for optimized search
export const searchCache = new LRUCache(CACHE.SEARCH_SIZE);
export const autocompleteCache = new LRUCache(CACHE.AUTOCOMPLETE_SIZE);